   <h2>My Design Philosophy</h2>

   <p>
      I generally follow a few tenets whenever I'm developing websites, doing maintenance, or adding new features to a website:
   </p>
   <br/>

   <ol>
      <li>
         <h3>First, Work Smart, then Hard</h3>
         <p>
            Working smarter means that thoughtful design before starting a task can
            lead to saving of time, as well as software and websites that can be easily modified later
            because the design decisions early on allow the adding of features later on. Working hard without
            figuring out the goal of the project leads to "virtual spinning of wheels".
         </p>
      </li>
      <li>
         <h3>Avoid Feature Creep</h3>
         <p>
            Add features when there's a meaningful reason to add them.
            I've learned during the course of my career that the best way to avoid feature creep is to add features as they're
            demanded from the user base.  As a web developer, this tenet comes especially in handy because
            the nature of website is that you're never really done, the website evolves with the user base as the
            users become acclimated to the website, and demand more features as needs crop up through the natural
            process of using a given website.
            I've actually found that adding features when they're not demanded often leads to wasted time as
            well as unuses or lackluster features.
         </p>
      </li>
      <li>
         <h3>Automate! Automate! Automate!</h3>
         <p>
            Whenever possible, automate tasks that are easily repeatable and reproducible. During my time at
            Reflexive Entertainment, I learned a lot about using technology and software to maximize productivity
            and save time.
         </p>
      </li>
      <li>
         <h3>Make it Modular!</h3>
         <p>
            Make software components modular so they can be reused and fitted into other applications later.
            I try to go with a "plug-n-play" approach to my modules, so that I can reuse modules that I've written
            earlier in other applications down the line.
         </p>
      
      </li>
   </ol>

</div>